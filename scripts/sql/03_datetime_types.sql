CREATE TABLE T_DATETIME_TYPES (
    ID                      NUMBER(10)                      NOT NULL,
    COL_DATE                DATE,
    COL_TIMESTAMP           TIMESTAMP,
    COL_TIMESTAMP3          TIMESTAMP(3),
    COL_TIMESTAMP6          TIMESTAMP(6),
    COL_TIMESTAMP9          TIMESTAMP(9),
    COL_TS_TZ               TIMESTAMP WITH TIME ZONE,
    COL_TS_LTZ              TIMESTAMP WITH LOCAL TIME ZONE,
    COL_INTERVAL_YM         INTERVAL YEAR TO MONTH,
    COL_INTERVAL_DS         INTERVAL DAY TO SECOND,
    CONSTRAINT PK_DATETIME_TYPES PRIMARY KEY (ID)
);

